let h = true;
let i = false;
let j = true;
let k = false;

// OR akan menjadi true jika salah satu pembanding bernilai true
console.log(h || i); 
// AND akan bernilai false jika salah satu pembanding bernilai false
console.log(h && i);

let m = 8;
let n = 7;

// Lebih besar
console.log(m > n);
// Pembanding diatas akan bernilai "true", karena variabel 'm' memang bernilai lebih besar daripada variabel 'n'

// Lebih kecil
console.log(m < n);
// Pembanding diatas akan bernilai "false", karena variabel 'm' tidak bernilai lebih kecil daripada variabel 'n'

// Sama dengan
console.log(m == n);
// Pembanding diatas akan bernilai "false", karena variabel 'm' memang bernilai sama daripada variabel 'n'

// Lebih besar sama dengan
console.log(m >= n);
// Pembanding diatas akan bernilai "true", karena variabel 'm' memang bernilai lebih besar sama dengan daripada variabel 'n'

// Lebih kecil sama dengan
console.log(m <= n);
// Pembanding diatas akan bernilai "false", karena variabel 'm' tidak bernilai lebih besar daripada variabel 'n'

// Tidak sama dengan
console.log(m != n);
// Pembanding diatas akan bernilai "true", karena variabel 'm' tidak bernilai sama dengan variabel 'n'
